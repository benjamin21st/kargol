function searchWithKeyWords(keyWords) {
  const results = db[keyWords] || [];
  const resultsPage = document.querySelector('#results-page');
  const resultHtmlParts = [];

  results.forEach(res => {
    let resHtml = `<li class="list-item"><a class="result-link" href="${res.link}">${res.title}</a><div>${res.description}</div></li>`;
    resultHtmlParts.push(resHtml);
  });

  resultsPage.innerHTML = `<ul id="results-list" class="uk-list">${resultHtmlParts.join('\n')}</ul>`;
}

document.addEventListener('DOMContentLoaded', () => {
  const searchBtn = document.querySelector('#search-button');
  const searchInput = document.querySelector('#search-input');

  searchInput.focus();

  searchBtn.addEventListener('click', (e) => {
    e.preventDefault();

    searchWithKeyWords(searchInput.value);
  });

  searchInput.addEventListener('keyup', (ev) => {
    if (ev.keyCode === 13) {
      searchWithKeyWords(searchInput.value);
    }
  })
});

